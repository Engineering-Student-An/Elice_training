package week1.ch1_4;

public class Training2 {
    public static void main(String[] args) {
        int count = 10;

        // 지시사항 1번을 참고하여 코드를 작성하세요.
        while(count >= 0) {
            if(count > 0) {
                System.out.println(count);
            } else {
                System.out.println("로켓 발사!");
            }
            count --;

        }
    }
}
